package com.example.javaproject2.week2.day4;

public class CompondAssignmentOperator {
    public static void main(String[] args) {
        int x = 10 ;
        x += 1; // x = x + 1
        System.out.printf("%d", x);
        x -= 5; // x = x - 5
        System.out.printf("%d", x);
        x *= 2; // x = x * 2
        System.out.printf("%d", x);
        x /= 3; // x = x / 3
        System.out.printf("%d", x);
        x %= 8; // x = x % 8
        System.out.printf("%d", x);
    }
}
