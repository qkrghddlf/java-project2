package com.example.javaproject2.codeup;

import java.util.Scanner;

public class Codeup1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int input;

        do {
            input = sc.nextInt();
            if (input != 0) System.out.println(input);
        } while (input != 0);
    }
}
